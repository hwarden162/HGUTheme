
# UKRI Colours ------------------------------------------------------------

#' HEX Code of the UKRI Dark Blue
#'
#' Access the HEX code of the dark blue used in the UKRI logo.
#'
#' @return The HEX code of the UKRI dark blue.
#' @export
#'
#' @examples
#'
#' barplot(
#'   1:3,
#'   col = ukri_dark_blue()
#' )
#'
ukri_dark_blue <- function() {
  "#2D2E5F"
}

